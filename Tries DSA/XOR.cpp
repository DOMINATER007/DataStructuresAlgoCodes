class Trie{
public:
  Trie* child[2];
  int mi;
  Trie(){
    child[0]=child[1]=NULL;
    mi=INT_MAX;
  }
};

Trie* root;

void insert(int x,int ind){
  Trie* cur=root;
  for(int i=29;i>=0;i--){
    int b=(x>>i)&1;
    if(cur->child[b]==NULL){
      cur->child[b]=new Trie();
    }
    cur=cur->child[b];
    cur->mi=min(ind,cur->mi);

  }
}

int query(int x,int k){
  Trie* cur=root;
  int ans=INT_MAX;
  for(int i=29;i>=0;i--){
    if(!cur) break;
    int kbit=(k>>i)&1;
    int xbit=(x>>i)&1;
    if(!kbit){
      if(cur->child[xbit^1]) ans=min(ans,cur->child[xbit^1]->mi);
      cur=cur->child[xbit];
    }
    else{
      cur=cur->child[xbit^1];
    }
  }
  if(cur) ans=min(ans,cur->mi);
  return ans;

}



void madara(){
  int n,k;
  cin>>n>>k;
  int pre=0;
  insert(pre,0);
  int ans=0,st=-1;
  for(int i=1;i<=n;i++){
    int x;
    cin>>x;
    pre^=x;
    int idx=query(pre,k);
    if(idx!=INT_MAX){
      if(i-idx>ans){
        ans=i-idx;
        st=idx;
      }
      else if(i-idx==ans){
        st=min(st,idx);
      }
    }
    insert(pre,i);
  }
  cout<<st+1<<" "<<ans<<endl;
  
  

}


